<?php

namespace Database\Factories;

use App\Interfaces\Models\UserInterface;
use Illuminate\Database\Eloquent\Factories\Factory;
use Illuminate\Support\Str;

/**
 * @extends \Illuminate\Database\Eloquent\Factories\Factory<\App\Models\User>
 */
class UserFactory extends Factory
{
    /**
     * Define the model's default state.
     *
     * @return array<string, mixed>
     */
    public function definition()
    {
        return [
            UserInterface::NAME => $this->faker->name(),
            UserInterface::EMAIL => $this->faker->unique()->safeEmail(),
            UserInterface::EMAIL_VERIFIED_AT => now(),
            UserInterface::PASSWORD => '$2y$10$92IXUNpkjO0rOQ5byMi.Ye4oKoEa3Ro9llC/.og/at2.uheWG/igi', // password
            UserInterface::REMEMBER_TOKEN => Str::random(10),
        ];
    }

    /**
     * Indicate that the model's email address should be unverified.
     *
     * @return static
     */
    public function unverified()
    {
        return $this->state(function (array $attributes) {
            return [
                UserInterface::EMAIL_VERIFIED_AT => null,
            ];
        });
    }
}
